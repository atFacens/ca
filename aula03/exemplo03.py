# operadores aritméticos + - / *

valor = ((2 + 3) * 2)

print(valor)

valor = ((2.0 + 3) * 2)

print(valor)

# operadores relacionais (comparação) < <= < <= == !=

print(10 > 5)
print(10 < 5)
print(10 != 5)
print(10 == 5)

# operadores lógicos and (E) or (OU) not (NOT)

print("10 > 5 and 10 > 2 ", 10 > 5 and 10 > 2) # só é true se ambos forem true
print("10 > 50 or 10 > 20",10 > 50 or 10 > 20) # só é false se ambos forem false
print("not 10 > 5", not 10 > 5) # só é false se ambos forem false
