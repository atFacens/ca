# variável é uma área de memória para guardar informações

# cria uma variável e armazena um número (int)
numero = 12

print(numero)

print(type(numero)) # int

# armazena um texto (string) na variável
numero = "Um texto"

print(numero)

print(type(numero)) # str

# Boa prática: use nomes significativos para as variáveis
nameUser = "Emerson"
